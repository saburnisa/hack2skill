        <div class="max-width">
          <h2 class="title">Diet Charts</h2>
          <div class="skills-content">
            <div class="column left">
              <div class="text">Start with hydration</div>
              <p>
                Alcohol is notorious for dehydrating the body, and many drug users do not feel the need to drink water while using.<br> Your body needs hydration to function well. In addition to lots of water, try electrolyte-packed beverages such as Gatorade and coconut water as well, especially if you’ve been vomiting.<br> If you’re able to make an investment, consider getting a juicer and plenty of fruits and vegetables to start getting vitamins back in your system while you hydrate.
              </p>
              <br>
              <div class="text">Protein</div>
              <p>
                Protein from both animal and plant sources are broken down into amino acids that the body uses to repair cells. Animal sources of protein like tuna, turkey and chicken are also great sources of vitamin B6. Red meat, pork and poultry provide iron, and fish like salmon are great sources of both omega-3 fatty acids and calcium. You can also get plant proteins from lentils, tofu (soy), black beans and quinoa.
              </p>
              <br>
              <div class="text">Complex carbohydrates</div>
              <p>
                Complex carbohydrates such as whole wheat flour, Dalia, oat, ragi, millets, etc provide steady and long lasting energy.This is important as the digestive system is adversely affected due to drug addiction. It is the major source of energy which is helpful to control addiction and recovery.
              </p><br>
              <div class="text">Reduce caffeine</div>
              <p>Caffeine containing foods such as coffee, tea, coca, chocolate, etc should be avoided because they may cause dehydration as well as appetite suppression.</p>
            </div>
